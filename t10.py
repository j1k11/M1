'''
Розробити програму, яка зчитує у користувача чотиризначне ціле число 
і відображає суму його цифр. Наприклад, якщо користувач вводить 3141,
то ваша програма повинна відображати 3 + 1 + 4 + 1 = 9.

'''
# Зчитати чотиризначне ціле число від користувача
number = int(input("Введіть чотиризначне ціле число: "))

# Розділити число на окремі цифри
digit_1 = number // 1000
digit_2 = (number % 1000) // 100
digit_3 = (number % 100) // 10
digit_4 = number % 10

# Обчислити суму цифр
sum_of_digits = digit_1 + digit_2 + digit_3 + digit_4

# Вивести результат
print(f"Сума цифр числа {number}: {digit_1} + {digit_2} + {digit_3} + {digit_4} = {sum_of_digits}")
