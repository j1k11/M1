'''
Напишіть програму, яка обчислює індекс маси тіла (ІМТ) людини. 
Ваша програма повинна починатися з читання зросту і ваги користувача. 
Потім слід використовувати одну з наступних двох формул для обчислення 
ІМТ перед його відображенням. Якщо читати зростання в дюймах і вага в фунтах, 
то індекс маси тіла розраховується за такою формулою:

                    BMI = (weight / height × height) × 703

Якщо читати зростання в метрах, а вага в кілограмах, 
то індекс маси тіла розраховується за такою трохи більш простою формулою:

                    BMI = (weight / height × height)

Користувачу потрібно надати можливість обрати одиниці виміру




'''
# Зчитати зріст та вагу від користувача
unit_choice = input("Виберіть одиниці виміру (1 - фунти та дюйми, 2 - кілограми та метри): ")

if unit_choice == "1":
    height_inches = float(input("Введіть зріст у дюймах: "))
    weight_pounds = float(input("Введіть вагу у фунтах: "))

    # Розрахунок ІМТ за першою формулою
    bmi = (weight_pounds / (height_inches * height_inches)) * 703
else:
    height_meters = float(input("Введіть зріст у метрах: "))
    weight_kg = float(input("Введіть вагу у кілограмах: "))

    # Розрахунок ІМТ за другою формулою
    bmi = weight_kg / (height_meters * height_meters)

# Вивести результат
print(f"Ваш ІМТ: {bmi:.2f}")
