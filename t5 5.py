'''
У багатьох юрисдикціях до ємностей для напоїв додається невеликий депозит, 
щоб заохотити людей переробляти їх. В одній юрисдикції контейнери для напоїв 
місткістю один літр або менше мають заставу в розмірі 0,10 дол. США, а контейнери
для напоїв місткістю понад один літр мають Депозит 0,25 дол.
Напишіть програму, яка зчитує кількість контейнерів кожного розміру 
від користувача. Ваша програма має продовжити обчислення та відображення 
відшкодування, яке буде отримані за повернення цих контейнерів. Відформатуйте
вихідні дані так, щоб вони містили знак долара і дві цифри праворуч від коми.
'''
# Задати ставки застави
застава_менше_1л = 0.10
застава_більше_1л = 0.25

# Зчитати кількість контейнерів від користувача
кількість_менше_1л = int(input("Введіть кількість контейнерів об'ємом 1 літр чи менше: "))
кількість_більше_1л = int(input("Введіть кількість контейнерів об'ємом більше 1 літра: "))

# Обчислити відшкодування
відшкодування_менше_1л = кількість_менше_1л * застава_менше_1л
відшкодування_більше_1л = кількість_більше_1л * застава_більше_1л
загальне_відшкодування = відшкодування_менше_1л + відшкодування_більше_1л

# Вивести результат з форматуванням валюти
print(f"Відшкодування за контейнери об'ємом 1 літр чи менше: ${відшкодування_менше_1л:.2f}")
print(f"Відшкодування за контейнери об'ємом більше 1 літра: ${відшкодування_більше_1л:.2f}")
print(f"Загальне відшкодування: ${загальне_відшкодування:.2f}")
